package JavaTutorial.FinalKeyword;

public class finalKeyword {
    public static void main(String[] args) {

        /*
         * if you declare a variable with the final keyword
         * you will not be able to change that variable
         * The variable becomes immutable
         * 
         * It is common practice is when you declare a variable as
         * final, the variable name is CAPITALIZED
         */

        final double PI = 3.14159;

        // pi = 3.14;

        System.out.println(PI);
    }
}
